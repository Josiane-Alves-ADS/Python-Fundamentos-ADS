# --- ESTRUTURA DE REPETIÇÃO FOR SIMPLES ---

# Itera sobre uma lista de números
numeros = [1, 2, 3, 4, 5]

for numero in numeros:
    print(numero)
    
print("-" * 10)

# Itera sobre uma lista de strings
filmes = ["Chuck", "Meu Malvado Favorito", "Super Mario"]

for filme in filmes:
    print(f"Filme: {filme}")
